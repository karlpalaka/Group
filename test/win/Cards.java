public class Cards {
    private String trainCards, destinationCards, train1, train2, destination1, destination2;
    
    public Cards()
    {
        trainCards = this.trainCards;
        destinationCards = this.destinationCards;
        train1 = this.train1;
        train2 = this.train2;
        destination1 = this.destination1;
        destination2 = this.destination2;
    }
    
    public String getTrainCards()
    {
        return trainCards;
    }
    
    public String getDestinationCards()
    {
        return destinationCards;
    }
    
    public String getTrain1()
    {
        return train1;
    }
    
    public String getTrain2()
    {
        return train2;
    }
    
    public String getDestination1()
    {
        return destination1;
    }
    
    public String getDestination2()
    {
        return destination2;
    }
    
            
    
}
